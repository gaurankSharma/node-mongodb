const mongoos = require("mongoose");
//todo list with validation
let todo = mongoos.model("todo", {
  text: {
    type: String,
    required: true,
    trim: true,
    minlength: 1
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

module.exports = { todo };
